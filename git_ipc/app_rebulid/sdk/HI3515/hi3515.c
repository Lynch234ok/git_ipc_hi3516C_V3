const char *sdk_strerror(uint32_t const errno)
{
	int i = 0;
	for(i = 0; i < (int)(sizeof(_hi3515_err_map) / sizeof(_hi3515_err_map[0])); ++i){
		if(errno == _hi3515_err_map[i].errno){
			return _hi3515_err_map[i].errstr;
		}
	}
	return "UNKNOWN ERROR!";
}


